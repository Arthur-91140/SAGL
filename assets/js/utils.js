// --- Check auth ---
function checkAuth() {
    const token = getToken();
    if (!token) {
        window.location.href = getBasePath() + '/admin/login.html';
        return false;
    }
    return true;
}

// --- Format date ---
function formatDate(dateStr) {
    if (!dateStr) return '-';
    const d = new Date(dateStr);
    return d.toLocaleDateString('fr-FR', {
        day: '2-digit',
        month: '2-digit',
        year: 'numeric',
        hour: '2-digit',
        minute: '2-digit',
    });
}

function formatDateShort(dateStr) {
    if (!dateStr) return '-';
    const d = new Date(dateStr);
    return d.toLocaleDateString('fr-FR');
}

// --- Escape HTML ---
function escapeHtml(str) {
    if (!str) return '';
    const div = document.createElement('div');
    div.textContent = str;
    return div.innerHTML;
}

// --- Init sidebar ---
function initSidebar() {
    const menuBtn = document.getElementById('menu-btn');
    const sidebar = document.getElementById('sidebar');
    const overlay = document.getElementById('sidebar-overlay');

    if (menuBtn && sidebar) {
        menuBtn.addEventListener('click', () => {
            sidebar.classList.toggle('open');
            if (overlay) overlay.classList.toggle('open');
        });
    }

    if (overlay) {
        overlay.addEventListener('click', () => {
            sidebar.classList.remove('open');
            overlay.classList.remove('open');
        });
    }

    // Highlight current nav item
    const currentPage = window.location.pathname.split('/').pop();
    document.querySelectorAll('.sidebar-nav a').forEach(a => {
        const href = a.getAttribute('href').split('/').pop();
        if (currentPage === href) {
            a.classList.add('active');
        }
    });

    // Load username
    const usernameEl = document.getElementById('topbar-username');
    if (usernameEl) {
        auth.me().then(data => {
            usernameEl.textContent = data.username;
        }).catch(() => {});
    }

    // Logout button
    const logoutBtn = document.getElementById('logout-btn');
    if (logoutBtn) {
        logoutBtn.addEventListener('click', () => {
            localStorage.removeItem('sagl_token');
            window.location.href = getBasePath() + '/admin/login.html';
        });
    }

    // Notification bell
    initNotificationBell();
}

// --- Notification bell ---
function initNotificationBell() {
    const bellBtn = document.getElementById('notification-btn');
    const dropdown = document.getElementById('notification-dropdown');
    const countEl = document.getElementById('notification-count');
    const listEl = document.getElementById('notification-list');

    if (!bellBtn || !dropdown) return;

    let isOpen = false;

    function loadNotifications() {
        notifications.get().then(data => {
            if (countEl) {
                if (data.count > 0) {
                    countEl.textContent = data.count > 99 ? '99+' : data.count;
                    countEl.style.display = 'flex';
                } else {
                    countEl.style.display = 'none';
                }
            }
            if (listEl) {
                if (data.notifications.length === 0) {
                    listEl.innerHTML = '<div style="padding:16px;text-align:center;color:var(--gray-500);font-size:14px">Aucune notification</div>';
                } else {
                    listEl.innerHTML = data.notifications.map(n =>
                        '<div class="notification-item ' + n.severity + '">' +
                        '<p style="font-weight:500">' + escapeHtml(n.message) + '</p>' +
                        '</div>'
                    ).join('');
                }
            }
        }).catch(() => {});
    }

    loadNotifications();
    setInterval(loadNotifications, 60000);

    bellBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        isOpen = !isOpen;
        dropdown.style.display = isOpen ? 'block' : 'none';
    });

    document.addEventListener('click', () => {
        isOpen = false;
        dropdown.style.display = 'none';
    });

    dropdown.addEventListener('click', (e) => e.stopPropagation());
}

// --- Greeting ---
function getGreetingMessage(type) {
    const hour = new Date().getHours();

    if (hour >= 5 && hour < 10) {
        switch (type) {
            case 'START': return 'Bonne matinée et bon début de mission !';
            case 'END': return "Bonne matinée ! On espère que tout s'est bien passé.";
            default: return 'Bonne matinée !';
        }
    }
    if (hour >= 10 && hour < 14) {
        switch (type) {
            case 'START': return 'Bonne journée et bon courage pour cette mission !';
            case 'END': return "Bonne fin de matinée ! On espère que la mission s'est bien déroulée.";
            default: return 'Bonne journée !';
        }
    }
    if (hour >= 14 && hour < 18) {
        switch (type) {
            case 'START': return "Bon après-midi et bon début de mission !";
            case 'END': return "Bon après-midi ! On espère que tout s'est bien passé.";
            default: return "Bon après-midi !";
        }
    }
    if (hour >= 18 && hour < 21) {
        switch (type) {
            case 'START': return 'Bonne soirée et bon courage pour cette mission !';
            case 'END': return "Bonne soirée ! On espère que la mission s'est bien déroulée.";
            default: return 'Bonne soirée !';
        }
    }
    switch (type) {
        case 'START': return 'Bonne nuit et bon courage pour cette mission !';
        case 'END': return "Bonne nuit ! On espère que tout s'est bien passé.";
        default: return 'Bonne nuit !';
    }
}
